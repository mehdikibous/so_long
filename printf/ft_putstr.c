#include "ft_printf.h"

void	ft_putstr(char *s, int *l)
{
	int	i;

	i = 0;
	if (s == NULL)
		s = "(null)";
	while (s[i])
	{
		ft_putchar(s[i], l);
		i++;
	}
}
