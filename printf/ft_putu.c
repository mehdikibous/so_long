#include "ft_printf.h"

void	ft_putu(unsigned int nb, int *l)
{
	if (nb < 10)
		ft_putchar(nb + '0', l);
	else
	{
		ft_putu(nb / 10, l);
		ft_putu(nb % 10, l);
	}
}
